export class LayoutSize {
    nzXs?: number;
    nzSm?: number;
    nzMd?: number;
    nzLg?: number;
    nzXXL?: number;
    constructor({ nzXs, nzSm, nzMd, nzLg, nzXXL }) {
        this.nzXs = nzXs;
        this.nzSm = nzSm;
        this.nzMd = nzMd;
        this.nzLg = nzLg;
        this.nzXXL = nzXXL;
    }
}